public class Hello {
    
    private String helloFrom;
    private String helloMessage;

    /**
     * @return the helloFrom
     */
    public String getHelloFrom() {
        return helloFrom;
    }

    /**
     * @param helloFrom the helloFrom to set
     */
    public void setHelloFrom(String helloFrom) {
        this.helloFrom = helloFrom;
    }

    /**
     * @return the helloMessage
     */
    public String getHelloMessage() {
        return helloMessage;
    }

    /**
     * @param helloMessage the helloMessage to set
     */
    public void setHelloMessage(String helloMessage) {
        this.helloMessage = helloMessage;
    }
}
